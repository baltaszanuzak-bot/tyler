document.addEventListener('DOMContentLoaded', () => {
    loadData();
    applyTheme();
    applyLang();

    // Lang buttons
    document.querySelectorAll('.lang-btn').forEach(b => b.onclick = () => {
        lang = b.dataset.lang;
        localStorage.setItem('fl_lang', lang);
        applyLang();
    });

    // Theme toggle
    document.getElementById('themeToggle').onclick = () => {
        theme = theme === 'light' ? 'dark' : 'light';
        localStorage.setItem('fl_theme', theme);
        applyTheme();
    };

    // Auth — redirect to login.html if not logged in
    if (!currentUser) {
        window.location.href = 'login.html';
        return;
    }

    // Show logged-in state
    document.getElementById('mainContent').style.display = 'block';
    document.getElementById('logoutBtn').style.display = 'inline-flex';
    document.getElementById('loginBtn').style.display = 'none';
    const rl = currentUser.role==='customer'?t('customerLabel'):t('freelancerLabel');
    document.getElementById('userStatus').innerHTML = `<i class="fas fa-user"></i> ${esc(currentUser.name)} (${rl})`;
    render();

    // Close modals on backdrop click
    document.querySelectorAll('.modal .close').forEach(b => b.onclick = () => b.closest('.modal').style.display = 'none');
    document.querySelectorAll('.modal').forEach(m => m.onclick = e => { if(e.target===m) m.style.display='none'; });

    // Nav
    document.querySelectorAll('.nav-btn').forEach(b => b.onclick = () => goToTab(b.dataset.tab));

    // "Go to feed" buttons (hero, sep)
    document.querySelectorAll('[data-goto="feed"]').forEach(b => b.onclick = () => goToTab('feed'));
    document.getElementById('heroCreateBtn').onclick = () => {
        if (!currentUser) return;
        if (currentUser.role !== 'customer') return alert(t('errCustomerOnly'));
        document.getElementById('createOrderModal').style.display = 'flex';
    };

    // Create order btn (sidebar)
    document.getElementById('createOrderBtn').onclick = () => {
        if (currentUser.role !== 'customer') return alert(t('errCustomerOnly'));
        document.getElementById('createOrderModal').style.display = 'flex';
    };

    document.getElementById('submitOrderBtn').onclick = () => {
        const title = document.getElementById('orderTitle').value.trim();
        const cat = document.getElementById('orderCategory').value;
        const desc = document.getElementById('orderDescription').value.trim();
        const budget = document.getElementById('orderBudget').value.trim();
        if (!title||!desc) return alert(t('errFields'));
        createOrder(title, cat, desc, budget);
        document.getElementById('createOrderModal').style.display = 'none';
        ['orderTitle','orderDescription','orderBudget'].forEach(id => document.getElementById(id).value='');
        render();
    };

    document.getElementById('submitResponseBtn').onclick = () => {
        const modal = document.getElementById('responseModal');
        const comment = document.getElementById('responseComment').value.trim();
        const price = document.getElementById('responsePrice').value.trim();
        if (!comment) return alert(t('errComment'));
        const ok = addResponse(modal.dataset.orderId, comment, price);
        if (!ok) return alert(t('alreadyResponded'));
        modal.style.display = 'none';
        document.getElementById('responseComment').value = '';
        document.getElementById('responsePrice').value = '';
        render();
    };

    document.getElementById('sendMessageBtn').onclick = () => {
        const modal = document.getElementById('chatModal');
        const inp = document.getElementById('chatInput');
        const text = inp.value.trim();
        if (!text) return;
        sendMessage(modal.dataset.orderId, text);
        inp.value = '';
        renderChatMessages(modal.dataset.orderId);
    };
    document.getElementById('chatInput').onkeydown = e => { if(e.key==='Enter') document.getElementById('sendMessageBtn').click(); };

    document.getElementById('submitReviewBtn').onclick = () => {
        const modal = document.getElementById('reviewModal');
        const rating = document.querySelector('.rating-stars span.active')?.dataset.rating || 5;
        const text = document.getElementById('reviewText').value.trim();
        const deal = deals.find(d => d.id === modal.dataset.dealId);
        addReview(modal.dataset.dealId, parseInt(rating), text, deal.freelancerId !== currentUser.id);
        modal.style.display = 'none';
        document.getElementById('reviewText').value = '';
        render();
    };

    // Stars
    document.querySelectorAll('.rating-stars span').forEach(star => {
        star.onclick = () => {
            document.querySelectorAll('.rating-stars span').forEach(s=>s.classList.remove('active'));
            let cur = star;
            while(cur) { cur.classList.add('active'); cur = cur.previousElementSibling; }
        };
    });

    // Clear orders
    document.getElementById('clearOrdersBtn').onclick = () => {
        if (!confirm(t('confirmClear'))) return;
        orders = []; responses = []; deals = []; messages = [];
        window.reviews = [];
        saveAll();
        render();
    };

    // Search & filter
    document.getElementById('searchOrders').addEventListener('input', e => { currentSearch=e.target.value; renderFeed(); });
    document.getElementById('categoryFilter').addEventListener('change', e => { currentCategory=e.target.value; renderFeed(); });
});

