function showResponsesList(orderId) {
    const list = responses.filter(r => r.orderId === orderId);
    if (!list.length) { alert(t('noResp')); return; }
    let msg = t('viewResponses')+':\n';
    list.forEach(r => { msg += `\n${r.freelancerName}: ${r.comment}\n${t('price')}: ${r.price}\n`; });
    const choice = prompt(msg+'\n'+t('chooseExec')+':\n'+list.map(r=>`${r.freelancerName} (ID: ${r.freelancerId})`).join('\n'));
    if (choice) {
        const sel = list.find(r => r.freelancerId === choice);
        if (sel && confirm(sel.freelancerName+'?')) { selectFreelancer(orderId, sel.freelancerId); render(); }
    }
}
