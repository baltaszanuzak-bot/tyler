let lang = localStorage.getItem('fl_lang') || 'ru';
let theme = localStorage.getItem('fl_theme') || 'light';

const t = k => T[lang]?.[k] || T.ru[k] || k;

function applyLang() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const k = el.getAttribute('data-i18n');
        const val = t(k);
        if (val.includes('<br>') || val.includes('<strong>')) el.innerHTML = val;
        else el.textContent = val;
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
    });
    document.querySelectorAll('.lang-btn').forEach(b => b.classList.toggle('active', b.dataset.lang === lang));
    if (currentUser) render();
}

function applyTheme() {
    document.documentElement.setAttribute('data-theme', theme);
    const ic = document.getElementById('themeIcon');
    if (ic) ic.className = theme === 'dark' ? 'fas fa-sun' : 'fas fa-moon';
}

// ── Data ──
let currentUser = null, orders = [], responses = [], deals = [], messages = [];

function loadData() {
    const u = localStorage.getItem('fl_user');
    if (u) currentUser = JSON.parse(u);
    orders = JSON.parse(localStorage.getItem('fl_orders') || '[]');
    responses = JSON.parse(localStorage.getItem('fl_responses') || '[]');
    deals = JSON.parse(localStorage.getItem('fl_deals') || '[]');
    messages = JSON.parse(localStorage.getItem('fl_messages') || '[]');
    window.reviews = JSON.parse(localStorage.getItem('fl_reviews') || '[]');
}
function saveAll() {
    if (currentUser) localStorage.setItem('fl_user', JSON.stringify(currentUser));
    localStorage.setItem('fl_orders', JSON.stringify(orders));
    localStorage.setItem('fl_responses', JSON.stringify(responses));
    localStorage.setItem('fl_deals', JSON.stringify(deals));
    localStorage.setItem('fl_messages', JSON.stringify(messages));
    localStorage.setItem('fl_reviews', JSON.stringify(window.reviews || []));
}
function uid() { return Date.now() + '-' + Math.random().toString(36).substr(2,6); }

function createOrder(title, category, description, budget) {
    if (!currentUser || currentUser.role !== 'customer') return false;
    orders.unshift({ id:uid(), title, category, description, budget:budget||'договорная', customer:currentUser.name, customerId:currentUser.id, status:'open', createdAt:new Date().toLocaleString(), chosenFreelancer:null });
    saveAll(); return true;
}
function addResponse(orderId, comment, price) {
    if (!currentUser || currentUser.role !== 'freelancer') return false;
    const order = orders.find(o => o.id === orderId);
    if (!order || order.status !== 'open') return false;
    if (responses.find(r => r.orderId === orderId && r.freelancerId === currentUser.id)) return false;
    responses.push({ id:uid(), orderId, freelancerId:currentUser.id, freelancerName:currentUser.name, comment, price:price||order.budget, createdAt:new Date().toLocaleString() });
    saveAll(); return true;
}
function selectFreelancer(orderId, freelancerId) {
    const order = orders.find(o => o.id === orderId);
    if (!order || order.customerId !== currentUser.id) return false;
    const resp = responses.find(r => r.orderId === orderId && r.freelancerId === freelancerId);
    if (!resp) return false;
    deals.push({ id:uid(), orderId, orderTitle:order.title, customerId:order.customerId, customerName:order.customer, freelancerId, freelancerName:resp.freelancerName, amount:resp.price, status:'in_progress', createdAt:new Date().toLocaleString(), completedAt:null, customerReviewed:false, freelancerReviewed:false });
    order.status = 'in_progress'; order.chosenFreelancer = freelancerId;
    saveAll(); return true;
}
function completeDeal(dealId) {
    const deal = deals.find(d => d.id === dealId);
    if (!deal || deal.customerId !== currentUser.id) return false;
    deal.status = 'completed'; deal.completedAt = new Date().toLocaleString();
    const order = orders.find(o => o.id === deal.orderId);
    if (order) order.status = 'completed';
    saveAll(); return true;
}
function addReview(dealId, rating, text, forFreelancer) {
    const deal = deals.find(d => d.id === dealId);
    if (!deal) return false;
    const targetId = forFreelancer ? deal.freelancerId : deal.customerId;
    if (!window.reviews) window.reviews = [];
    window.reviews.push({ id:uid(), targetId, authorId:currentUser.id, authorName:currentUser.name, rating, text, createdAt:new Date().toLocaleString() });
    if (forFreelancer) deal.customerReviewed = true; else deal.freelancerReviewed = true;
    saveAll(); return true;
}
function sendMessage(orderId, text) {
    if (!currentUser) return false;
    messages.push({ id:uid(), orderId, senderId:currentUser.id, senderName:currentUser.name, text, timestamp:new Date().toLocaleString() });
    saveAll(); return true;
}
function getMessages(orderId) { return messages.filter(m => m.orderId === orderId); }
function getUserRating(userId) {
    const rv = (window.reviews||[]).filter(r => r.targetId === userId);
    if (!rv.length) return null;
    return (rv.reduce((s,r)=>s+r.rating,0)/rv.length).toFixed(1);
}

// ── Render ──
let currentSearch = '', currentCategory = '';

function render() {
    renderProfile();
    updateStats();
    const active = document.querySelector('.nav-btn.active')?.dataset.tab;
    if (active === 'home') renderHome();
    else if (active === 'feed') renderFeed();
    else if (active === 'myOrders') renderMyOrders();
    else if (active === 'myResponses') renderMyResponses();
    else if (active === 'deals') renderDeals();
}

function renderProfile() {
    if (!currentUser) return;
    const rating = getUserRating(currentUser.id);
    const roleLabel = currentUser.role === 'customer' ? t('customerLabel') : t('freelancerLabel');
    const stars = rating ? '★'.repeat(Math.round(rating))+'☆'.repeat(5-Math.round(rating)) : '';
    document.getElementById('profileCard').innerHTML = `
        <div class="avatar"><i class="fas fa-user"></i></div>
        <div class="profile-name">${esc(currentUser.name)}</div>
        <div class="profile-role">${roleLabel}</div>
        ${rating ? `<div class="rating">${stars} <small>${rating}</small></div>` : `<div class="rating" style="font-size:.78rem;color:var(--text-muted)">${t('noRating')}</div>`}
    `;
}

function updateStats() {
    const so = document.getElementById('statOrders');
    const sr = document.getElementById('statResponses');
    const sd = document.getElementById('statDeals');
    if (so) so.textContent = orders.length;
    if (sr) sr.textContent = responses.length;
    if (sd) sd.textContent = deals.length;
    const fc = document.getElementById('feedCount');
    if (fc) fc.textContent = orders.length;
}

function statusLabel(s) {
    if (s==='open') return t('statusOpen');
    if (s==='in_progress') return t('statusInProgress');
    if (s==='completed') return t('statusCompleted');
    return s;
}

function orderCardHTML(order, preview=false) {
    const isOwner = currentUser && order.customerId === currentUser.id;
    const hasResp = responses.some(r => r.orderId === order.id && r.freelancerId === currentUser?.id);
    const respCount = responses.filter(r => r.orderId === order.id).length;
    return `
    <div class="order-card">
        <div class="cat-badge">${esc(order.category)}</div>
        <div class="order-title">${esc(order.title)}</div>
        <div class="order-meta">
            <span><i class="fas fa-user"></i>${esc(order.customer)}</span>
            <span class="budget"><i class="fas fa-ruble-sign"></i>${esc(String(order.budget))}</span>
            <span><i class="fas fa-clock"></i>${order.createdAt}</span>
        </div>
        <div class="description">${esc(order.description)}</div>
        <div class="card-actions">
            <span class="status-badge status-${order.status}">${statusLabel(order.status)}</span>
            ${isOwner && order.status==='open' && respCount>0 ? `<button class="btn-outline btn-sm" data-order="${order.id}" data-action="viewResp"><i class="fas fa-users"></i> ${t('viewResponses')} (${respCount})</button>` : ''}
            ${!isOwner && currentUser?.role==='freelancer' && order.status==='open' && !hasResp ? `<button class="btn-accent btn-sm" data-order="${order.id}" data-action="respond"><i class="fas fa-paper-plane"></i> ${t('respond')}</button>` : ''}
            ${!isOwner && hasResp ? `<span style="font-size:.76rem;color:var(--text-muted)"><i class="fas fa-check"></i> ${t('alreadyResponded')}</span>` : ''}
        </div>
    </div>`;
}

function attachCardEvents(container) {
    container.querySelectorAll('[data-action="respond"]').forEach(b => b.onclick = () => openResponseModal(b.dataset.order));
    container.querySelectorAll('[data-action="viewResp"]').forEach(b => b.onclick = () => showResponsesList(b.dataset.order));
}

function renderHome() {
    updateStats();
    const preview = document.getElementById('previewList');
    if (!preview) return;
    const list = orders.slice(0,3);
    if (!list.length) {
        preview.innerHTML = `<div class="empty-state" style="grid-column:1/-1"><i class="fas fa-inbox"></i><p>${t('noOrders')}</p></div>`;
        return;
    }
    preview.innerHTML = list.map(o => orderCardHTML(o, true)).join('');
    attachCardEvents(preview);
}

function renderFeed() {
    const container = document.getElementById('projectsList');
    let list = [...orders];
    if (currentSearch) list = list.filter(o => o.title.toLowerCase().includes(currentSearch.toLowerCase()));
    if (currentCategory) list = list.filter(o => o.category === currentCategory);
    const fc = document.getElementById('feedCount');
    if (fc) fc.textContent = list.length;
    if (!list.length) {
        container.innerHTML = `<div class="empty-state" style="grid-column:1/-1"><i class="fas fa-search"></i><p>${t('noOrders')}</p></div>`;
        return;
    }
    container.innerHTML = list.map(o => orderCardHTML(o)).join('');
    attachCardEvents(container);
}

function renderMyOrders() {
    const c = document.getElementById('myOrdersList');
    const list = orders.filter(o => o.customerId === currentUser.id);
    if (!list.length) { c.innerHTML = `<div class="empty-state"><i class="fas fa-folder-open"></i><p>${t('myOrdersEmpty')}</p></div>`; return; }
    c.innerHTML = list.map(o => orderCardHTML(o)).join('');
    attachCardEvents(c);
}

