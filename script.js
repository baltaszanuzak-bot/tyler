function openChatModal(orderId, title) {
    const modal = document.getElementById('chatModal');
    document.getElementById('chatOrderTitle').textContent = title;
    modal.dataset.orderId = orderId; modal.style.display = 'flex';
    renderChatMessages(orderId);
}
function renderChatMessages(orderId) {
    const c = document.getElementById('chatMessages');
    const msgs = getMessages(orderId);
    c.innerHTML = msgs.map(m=>`<div class="message ${m.senderId===currentUser.id?'own':''}"><strong>${esc(m.senderName)}</strong>${esc(m.text)}<small>${m.timestamp}</small></div>`).join('');
    c.scrollTop = c.scrollHeight;
}
function openReviewModal(dealId) {
    const deal = deals.find(d => d.id === dealId);
    const modal = document.getElementById('reviewModal');
    document.getElementById('reviewFreelancerName').textContent = deal.freelancerId===currentUser.id ? deal.customerName : deal.freelancerName;
    modal.dataset.dealId = dealId; modal.style.display = 'flex';
}

function esc(s) { if(!s)return''; return s.replace(/[&<>"']/g,m=>({'&':'&amp;','<':'&lt;','>':'&gt;','"':'&quot;',"'":'&#39;'}[m])); }

// ── Navigation helper ──
function goToTab(tabName) {
    document.querySelectorAll('.nav-btn').forEach(b => b.classList.remove('active'));
    const btn = document.querySelector(`.nav-btn[data-tab="${tabName}"]`);
    if (btn) btn.classList.add('active');
    document.querySelectorAll('.tab-content').forEach(t => t.classList.remove('active'));
    const tabMap = { home:'homeTab', feed:'feedTab', myOrders:'myOrdersTab', myResponses:'myResponsesTab', deals:'dealsTab' };
    const el = document.getElementById(tabMap[tabName]);
    if (el) el.classList.add('active');
    render();
}

